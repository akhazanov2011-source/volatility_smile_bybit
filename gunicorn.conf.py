# Конфигурация gunicorn для volatility_smile_bybit.
#
# Раньше воркер запускался «голым» `gunicorn app:app`, что оставляло все
# настройки дефолтными (1 sync-воркер, --timeout 30). Маршрут index() при
# промахе кеша синхронно обращался к Bybit с ретраями (~45с в худшем случае),
# поэтому arbiter регулярно убивал воркер по WORKER TIMEOUT.
#
# Теперь путь запроса не блокируется на сети (cache-miss отдаётся мгновенно
# страницей «прогрев кеша», а тяжёлая работа уходит в фоновый поток), но
# gunicorn всё равно должен быть устойчивым: потокобезопасный worker-класс,
# несколько воркеров/потоков и щедрый таймаут как страховка.

bind = "0.0.0.0:8000"

# gthread обрабатывает несколько запросов одновременно в каждом воркере, поэтому
# медленный фоновый поток прогрева кеша не блокирует /healthz и соседние запросы.
worker_class = "gthread"
workers = 2
threads = 4

# Щедрый запас: даже если какой-то запрос окажется медленным, arbiter не убьёт
# воркер. С неблокирующим index() запросы обычно завершаются за миллисекунды,
# 90с — это чистая страховка против WORKER TIMEOUT.
timeout = 90
graceful_timeout = 30

# Периодическая перезагрузка воркеров против накопления утечек памяти
# (Plotly-рендер 66 комбинаций каждые 3 минуты расходует память).
max_requests = 1000
max_requests_jitter = 100

# ВАЖНО: НЕ включать --preload. Фоновый поток кеша стартует при импорте app.py
# в теле каждого воркера. С preload поток бы запустился в master-процессе, а
# после fork() в воркерах не пережил бы — кеширование бы молча сломалось,
# потому что потоки не наследуются через fork().

accesslog = "-"
errorlog = "-"
loglevel = "info"
